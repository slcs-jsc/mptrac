void get_met(
  ctl_t * ctl,
  char *metbase,
  double t,
  met_t ** met0,
  met_t ** met1) {

  static int init, ip, ix, iy;

  met_t *mets;

  char filename[LEN];

  /* Init... */
  if (t == ctl->t_start || !init) {
    init = 1;

    get_met_help(t, -1, metbase, ctl->dt_met, filename);
    if (!read_met(ctl, filename, *met0))
      ERRMSG("Cannot open file!");

    get_met_help(t + 1.0 * ctl->direction, 1, metbase, ctl->dt_met, filename);
    if (!read_met(ctl, filename, *met1))
      ERRMSG("Cannot open file!");
  }

  /* Read new data for forward trajectories... */
  if (t > (*met1)->time && ctl->direction == 1) {
    mets = *met1;
    *met1 = *met0;
    *met0 = mets;
    get_met_help(t, 1, metbase, ctl->dt_met, filename);
    if (!read_met(ctl, filename, *met1))
      ERRMSG("Cannot open file!");
  }

  /* Read new data for backward trajectories... */
  if (t < (*met0)->time && ctl->direction == -1) {
    mets = *met1;
    *met1 = *met0;
    *met0 = mets;
    get_met_help(t, -1, metbase, ctl->dt_met, filename);
    if (!read_met(ctl, filename, *met0))
      ERRMSG("Cannot open file!");
  }

  /* Check that grids are consistent... */
  if ((*met0)->nx != (*met1)->nx
      || (*met0)->ny != (*met1)->ny || (*met0)->np != (*met1)->np)
    ERRMSG("Meteo grid dimensions do not match!");
  for (ix = 0; ix < (*met0)->nx; ix++)
    if ((*met0)->lon[ix] != (*met1)->lon[ix])
      ERRMSG("Meteo grid longitudes do not match!");
  for (iy = 0; iy < (*met0)->ny; iy++)
    if ((*met0)->lat[iy] != (*met1)->lat[iy])
      ERRMSG("Meteo grid latitudes do not match!");
  for (ip = 0; ip < (*met0)->np; ip++)
    if ((*met0)->p[ip] != (*met1)->p[ip])
      ERRMSG("Meteo grid pressure levels do not match!");
}

/*****************************************************************************/

void get_met_help(
  double t,
  int direct,
  char *metbase,
  double dt_met,
  char *filename) {

  char repl[LEN];

  double t6, r;

  int year, mon, day, hour, min, sec;

  /* Round time to fixed intervals... */
  if (direct == -1)
    t6 = floor(t / dt_met) * dt_met;
  else
    t6 = ceil(t / dt_met) * dt_met;

  /* Decode time... */
  jsec2time(t6, &year, &mon, &day, &hour, &min, &sec, &r);

  /* Set filename... */
  sprintf(filename, "%s_YYYY_MM_DD_HH.nc", metbase);
  sprintf(repl, "%d", year);
  get_met_replace(filename, "YYYY", repl);
  sprintf(repl, "%02d", mon);
  get_met_replace(filename, "MM", repl);
  sprintf(repl, "%02d", day);
  get_met_replace(filename, "DD", repl);
  sprintf(repl, "%02d", hour);
  get_met_replace(filename, "HH", repl);
}

/*****************************************************************************/

void get_met_replace(
  char *orig,
  char *search,
  char *repl) {

  char buffer[LEN], *ch;

  int i;

  /* Iterate... */
  for (i = 0; i < 3; i++) {

    /* Replace substring... */
    if (!(ch = strstr(orig, search)))
      return;
    strncpy(buffer, orig, (size_t) (ch - orig));
    buffer[ch - orig] = 0;
    sprintf(buffer + (ch - orig), "%s%s", repl, ch + strlen(search));
    orig[0] = 0;
    strcpy(orig, buffer);
  }
}

/*****************************************************************************/

double intpol_met_2d(
  double array[EX][EY],
  int ix,
  int iy,
  double wx,
  double wy) {

  double aux00, aux01, aux10, aux11;

  /* Set variables... */
  aux00 = array[ix][iy];
  aux01 = array[ix][iy + 1];
  aux10 = array[ix + 1][iy];
  aux11 = array[ix + 1][iy + 1];

  /* Interpolate horizontally... */
  aux00 = wy * (aux00 - aux01) + aux01;
  aux11 = wy * (aux10 - aux11) + aux11;
  return wx * (aux00 - aux11) + aux11;
}

/*****************************************************************************/

double intpol_met_3d(
  float array[EX][EY][EP],
  int ip,
  int ix,
  int iy,
  double wp,
  double wx,
  double wy) {

  double aux00, aux01, aux10, aux11;

  /* Interpolate vertically... */
  aux00 = wp * (array[ix][iy][ip] - array[ix][iy][ip + 1])
    + array[ix][iy][ip + 1];
  aux01 = wp * (array[ix][iy + 1][ip] - array[ix][iy + 1][ip + 1])
    + array[ix][iy + 1][ip + 1];
  aux10 = wp * (array[ix + 1][iy][ip] - array[ix + 1][iy][ip + 1])
    + array[ix + 1][iy][ip + 1];
  aux11 = wp * (array[ix + 1][iy + 1][ip] - array[ix + 1][iy + 1][ip + 1])
    + array[ix + 1][iy + 1][ip + 1];

  /* Interpolate horizontally... */
  aux00 = wy * (aux00 - aux01) + aux01;
  aux11 = wy * (aux10 - aux11) + aux11;
  return wx * (aux00 - aux11) + aux11;
}

/*****************************************************************************/

//#pragma acc routine seq
void intpol_met_space(
  met_t * met,
  double p,
  double lon,
  double lat,
  double *ps,
  double *pt,
  double *z,
  double *t,
  double *u,
  double *v,
  double *w,
  double *pv,
  double *h2o,
  double *o3) {

  double wp, wx, wy;

  int ip, ix, iy;

  /* Check longitude... */
  if (met->lon[met->nx - 1] > 180 && lon < 0)
    lon += 360;

  /* Get indices... */
  ip = locate_irr(met->p, met->np, p);
  ix = locate_reg(met->lon, met->nx, lon);
  iy = locate_reg(met->lat, met->ny, lat);

  /* Get weights... */
  wp = (met->p[ip + 1] - p) / (met->p[ip + 1] - met->p[ip]);
  wx = (met->lon[ix + 1] - lon) / (met->lon[ix + 1] - met->lon[ix]);
  wy = (met->lat[iy + 1] - lat) / (met->lat[iy + 1] - met->lat[iy]);

  /* Interpolate... */
  if (ps != NULL)
    *ps = intpol_met_2d(met->ps, ix, iy, wx, wy);
  if (pt != NULL)
    *pt = intpol_met_2d(met->pt, ix, iy, wx, wy);
  if (z != NULL)
    *z = intpol_met_3d(met->z, ip, ix, iy, wp, wx, wy);
  if (t != NULL)
    *t = intpol_met_3d(met->t, ip, ix, iy, wp, wx, wy);
  if (u != NULL)
    *u = intpol_met_3d(met->u, ip, ix, iy, wp, wx, wy);
  if (v != NULL)
    *v = intpol_met_3d(met->v, ip, ix, iy, wp, wx, wy);
  if (w != NULL)
    *w = intpol_met_3d(met->w, ip, ix, iy, wp, wx, wy);
  if (pv != NULL)
    *pv = intpol_met_3d(met->pv, ip, ix, iy, wp, wx, wy);
  if (h2o != NULL)
    *h2o = intpol_met_3d(met->h2o, ip, ix, iy, wp, wx, wy);
  if (o3 != NULL)
    *o3 = intpol_met_3d(met->o3, ip, ix, iy, wp, wx, wy);
}

/*****************************************************************************/

//#pragma acc routine seq
void intpol_met_time(
  met_t * met0,
  met_t * met1,
  double ts,
  double p,
  double lon,
  double lat,
  double *ps,
  double *pt,
  double *z,
  double *t,
  double *u,
  double *v,
  double *w,
  double *pv,
  double *h2o,
  double *o3) {

  double h2o0, h2o1, o30, o31, ps0, ps1, pt0, pt1, pv0, pv1, t0, t1, u0, u1,
    v0, v1, w0, w1, wt, z0, z1;

  /* Spatial interpolation... */
  intpol_met_space(met0, p, lon, lat,
		   ps == NULL ? NULL : &ps0,
		   pt == NULL ? NULL : &pt0,
		   z == NULL ? NULL : &z0,
		   t == NULL ? NULL : &t0,
		   u == NULL ? NULL : &u0,
		   v == NULL ? NULL : &v0,
		   w == NULL ? NULL : &w0,
		   pv == NULL ? NULL : &pv0,
		   h2o == NULL ? NULL : &h2o0, o3 == NULL ? NULL : &o30);
  intpol_met_space(met1, p, lon, lat,
		   ps == NULL ? NULL : &ps1,
		   pt == NULL ? NULL : &pt1,
		   z == NULL ? NULL : &z1,
		   t == NULL ? NULL : &t1,
		   u == NULL ? NULL : &u1,
		   v == NULL ? NULL : &v1,
		   w == NULL ? NULL : &w1,
		   pv == NULL ? NULL : &pv1,
		   h2o == NULL ? NULL : &h2o1, o3 == NULL ? NULL : &o31);

  /* Get weighting factor... */
  wt = (met1->time - ts) / (met1->time - met0->time);

  /* Interpolate... */
  if (ps != NULL)
    *ps = wt * (ps0 - ps1) + ps1;
  if (pt != NULL)
    *pt = wt * (pt0 - pt1) + pt1;
  if (z != NULL)
    *z = wt * (z0 - z1) + z1;
  if (t != NULL)
    *t = wt * (t0 - t1) + t1;
  if (u != NULL)
    *u = wt * (u0 - u1) + u1;
  if (v != NULL)
    *v = wt * (v0 - v1) + v1;
  if (w != NULL)
    *w = wt * (w0 - w1) + w1;
  if (pv != NULL)
    *pv = wt * (pv0 - pv1) + pv1;
  if (h2o != NULL)
    *h2o = wt * (h2o0 - h2o1) + h2o1;
  if (o3 != NULL)
    *o3 = wt * (o30 - o31) + o31;
}

/*****************************************************************************/

void jsec2time(
  double jsec,
  int *year,
  int *mon,
  int *day,
  int *hour,
  int *min,
  int *sec,
  double *remain) {

  struct tm t0, *t1;

  time_t jsec0;

  t0.tm_year = 100;
  t0.tm_mon = 0;
  t0.tm_mday = 1;
  t0.tm_hour = 0;
  t0.tm_min = 0;
  t0.tm_sec = 0;

  jsec0 = (time_t) jsec + timegm(&t0);
  t1 = gmtime(&jsec0);

  *year = t1->tm_year + 1900;
  *mon = t1->tm_mon + 1;
  *day = t1->tm_mday;
  *hour = t1->tm_hour;
  *min = t1->tm_min;
  *sec = t1->tm_sec;
  *remain = jsec - floor(jsec);
}

/*****************************************************************************/

#pragma acc routine seq
int locate_irr(
  double *xx,
  int n,
  double x) {

  int i, ilo, ihi;

  ilo = 0;
  ihi = n - 1;
  i = (ihi + ilo) >> 1;

  if (xx[i] < xx[i + 1])
    while (ihi > ilo + 1) {
      i = (ihi + ilo) >> 1;
      if (xx[i] > x)
	ihi = i;
      else
	ilo = i;
  } else
    while (ihi > ilo + 1) {
      i = (ihi + ilo) >> 1;
      if (xx[i] <= x)
	ihi = i;
      else
	ilo = i;
    }

  return ilo;
}

/*****************************************************************************/

#pragma acc routine seq
int locate_reg(
  double *xx,
  int n,
  double x) {

  int i;

  /* Calculate index... */
  i = (int) ((x - xx[0]) / (xx[1] - xx[0]));

  /* Check range... */
  if (i < 0)
    i = 0;
  else if (i >= n - 2)
    i = n - 2;

  return i;
}

/*****************************************************************************/

int read_atm(
  const char *filename,
  ctl_t * ctl,
  atm_t * atm) {

  FILE *in;

  char line[LEN], *tok;

  double t0;

  int dimid, ip, iq, ncid, varid;

  size_t nparts;

  /* Init... */
  atm->np = 0;

  /* Write info... */
  printf("Read atmospheric data: %s\n", filename);

  /* Read ASCII data... */
  if (ctl->atm_type == 0) {

    /* Open file... */
    if (!(in = fopen(filename, "r"))) {
      WARN("File not found!");
      return 0;
    }

    /* Read line... */
    while (fgets(line, LEN, in)) {

      /* Read data... */
      TOK(line, tok, "%lg", atm->time[atm->np]);
      TOK(NULL, tok, "%lg", atm->p[atm->np]);
      TOK(NULL, tok, "%lg", atm->lon[atm->np]);
      TOK(NULL, tok, "%lg", atm->lat[atm->np]);
      for (iq = 0; iq < ctl->nq; iq++)
	TOK(NULL, tok, "%lg", atm->q[iq][atm->np]);

      /* Convert altitude to pressure... */
      atm->p[atm->np] = P(atm->p[atm->np]);

      /* Increment data point counter... */
      if ((++atm->np) > NP)
	ERRMSG("Too many data points!");
    }

    /* Close file... */
    fclose(in);
  }

  /* Read binary data... */
  else if (ctl->atm_type == 1) {

    /* Open file... */
    if (!(in = fopen(filename, "r")))
      return 0;

    /* Read data... */
    FREAD(&atm->np, int, 1, in);
    FREAD(atm->time, double,
	    (size_t) atm->np,
	  in);
    FREAD(atm->p, double,
	    (size_t) atm->np,
	  in);
    FREAD(atm->lon, double,
	    (size_t) atm->np,
	  in);
    FREAD(atm->lat, double,
	    (size_t) atm->np,
	  in);
    for (iq = 0; iq < ctl->nq; iq++)
      FREAD(atm->q[iq], double,
	      (size_t) atm->np,
	    in);

    /* Close file... */
    fclose(in);
  }

  /* Read netCDF data... */
  else if (ctl->atm_type == 2) {

    /* Open file... */
    if (nc_open(filename, NC_NOWRITE, &ncid) != NC_NOERR)
      return 0;

    /* Get dimensions... */
    NC(nc_inq_dimid(ncid, "NPARTS", &dimid));
    NC(nc_inq_dimlen(ncid, dimid, &nparts));
    atm->np = (int) nparts;
    if (atm->np > NP)
      ERRMSG("Too many particles!");

    /* Get time... */
    NC(nc_inq_varid(ncid, "time", &varid));
    NC(nc_get_var_double(ncid, varid, &t0));
    for (ip = 0; ip < atm->np; ip++)
      atm->time[ip] = t0;

    /* Read geolocations... */
    NC(nc_inq_varid(ncid, "PRESS", &varid));
    NC(nc_get_var_double(ncid, varid, atm->p));
    NC(nc_inq_varid(ncid, "LON", &varid));
    NC(nc_get_var_double(ncid, varid, atm->lon));
    NC(nc_inq_varid(ncid, "LAT", &varid));
    NC(nc_get_var_double(ncid, varid, atm->lat));

    /* Read variables... */
    if (ctl->qnt_p >= 0)
      if (nc_inq_varid(ncid, "PRESS", &varid) == NC_NOERR)
	NC(nc_get_var_double(ncid, varid, atm->q[ctl->qnt_p]));
    if (ctl->qnt_t >= 0)
      if (nc_inq_varid(ncid, "TEMP", &varid) == NC_NOERR)
	NC(nc_get_var_double(ncid, varid, atm->q[ctl->qnt_t]));
    if (ctl->qnt_u >= 0)
      if (nc_inq_varid(ncid, "U", &varid) == NC_NOERR)
	NC(nc_get_var_double(ncid, varid, atm->q[ctl->qnt_u]));
    if (ctl->qnt_v >= 0)
      if (nc_inq_varid(ncid, "V", &varid) == NC_NOERR)
	NC(nc_get_var_double(ncid, varid, atm->q[ctl->qnt_v]));
    if (ctl->qnt_w >= 0)
      if (nc_inq_varid(ncid, "W", &varid) == NC_NOERR)
	NC(nc_get_var_double(ncid, varid, atm->q[ctl->qnt_w]));
    if (ctl->qnt_h2o >= 0)
      if (nc_inq_varid(ncid, "SH", &varid) == NC_NOERR)
	NC(nc_get_var_double(ncid, varid, atm->q[ctl->qnt_h2o]));
    if (ctl->qnt_o3 >= 0)
      if (nc_inq_varid(ncid, "O3", &varid) == NC_NOERR)
	NC(nc_get_var_double(ncid, varid, atm->q[ctl->qnt_o3]));
    if (ctl->qnt_theta >= 0)
      if (nc_inq_varid(ncid, "THETA", &varid) == NC_NOERR)
	NC(nc_get_var_double(ncid, varid, atm->q[ctl->qnt_theta]));
    if (ctl->qnt_pv >= 0)
      if (nc_inq_varid(ncid, "PV", &varid) == NC_NOERR)
	NC(nc_get_var_double(ncid, varid, atm->q[ctl->qnt_pv]));

    /* Check data... */
    for (ip = 0; ip < atm->np; ip++)
      if (fabs(atm->lon[ip]) > 360 || fabs(atm->lat[ip]) > 90
	  || (ctl->qnt_t >= 0 && fabs(atm->q[ctl->qnt_t][ip]) > 350)
	  || (ctl->qnt_h2o >= 0 && fabs(atm->q[ctl->qnt_h2o][ip]) > 1)
	  || (ctl->qnt_theta >= 0 && fabs(atm->q[ctl->qnt_theta][ip]) > 1e10)
	  || (ctl->qnt_pv >= 0 && fabs(atm->q[ctl->qnt_pv][ip]) > 1e10)) {
	atm->time[ip] = GSL_NAN;
	atm->p[ip] = GSL_NAN;
	atm->lon[ip] = GSL_NAN;
	atm->lat[ip] = GSL_NAN;
	for (iq = 0; iq < ctl->nq; iq++)
	  atm->q[iq][ip] = GSL_NAN;
      } else {
	if (ctl->qnt_h2o >= 0)
	  atm->q[ctl->qnt_h2o][ip] *= 1.608;
	if (ctl->qnt_pv >= 0)
	  atm->q[ctl->qnt_pv][ip] *= 1e6;
	if (atm->lon[ip] > 180)
	  atm->lon[ip] -= 360;
      }

    /* Close file... */
    NC(nc_close(ncid));
  }

  /* Error... */
  else
    ERRMSG("Atmospheric data type not supported!");

  /* Check number of points... */
  if (atm->np < 1)
    ERRMSG("Can not read any data!");

  /* Return success... */
  return 1;
}

